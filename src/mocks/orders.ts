export const ORDER_STATUSES = ["待完成", "已完成"] as const;

export type OrderStatus = (typeof ORDER_STATUSES)[number];

export const PAYMENT_METHODS = [
  "待确认",
  "微信",
  "支付宝",
  "银行转账",
  "现金",
  "其他",
] as const;

export type PaymentMethod = (typeof PAYMENT_METHODS)[number];

export interface OrderPaymentVoucher {
  fileName: string;
  mimeType: string;
  dataUrl: string;
}

export interface OrderListItem {
  id: string;
  studentId: string;
  packageId: string;
  amount: number;
  status: OrderStatus;
  /** Local wall time minute precision, e.g. `2026-03-12T10:18` */
  orderDate: string;
  paymentMethod: PaymentMethod;
  /** Minute precision when payment is confirmed */
  paymentDate?: string;
  paymentVoucher?: OrderPaymentVoucher;
  closedAt?: string;
  note?: string;
  createdAt: string;
  updatedAt: string;
}

interface PaymentCompletionInput {
  paymentMethod: PaymentMethod;
  paymentDate?: string;
  paymentVoucher?: OrderPaymentVoucher;
}

export function hasCompletePayment(input: PaymentCompletionInput): boolean {
  return (
    input.paymentMethod !== "待确认" &&
    Boolean(input.paymentDate) &&
    Boolean(input.paymentVoucher?.dataUrl)
  );
}

export function deriveOrderStatus(input: PaymentCompletionInput): OrderStatus {
  return hasCompletePayment(input) ? "已完成" : "待完成";
}

